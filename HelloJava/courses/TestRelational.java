package courses;

public class TestRelational {

	public static void main(String[] args) {
		int x=100, y=50;
		
		System.out.println("Is " + x + " = " + y + " ? " + (x==y));
		System.out.println("Is " + x + " != " + y + " ? " + (x!=y));
		System.out.println("Is " + x + " > "+ y + " ? " + (x>y));
		System.out.println("Is " + x + " >= "+ y + " ? " + (x>=y));
		System.out.println("Is " + x + " < "+ y + " ? " + (x<y));
		System.out.println("Is " + x + " <= "+ y + " ? " + (x<=y));
		
	}

}
